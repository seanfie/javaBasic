package com.neuedu.seven;

class Post
{
    String name;
    String street;
    String city;
    String province;
    String country;

    public Post(String name, String street, String city, String province, String country) {

        this.name = name;
        this.street = street;
        this.city = city;
        this.province = province;
        this.country = country;

    }
}